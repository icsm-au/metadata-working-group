package org.fao.geonet.services.notifications.domain;

/**
 * Represents a notification target.
 *
 * @author heikki doeleman
 */
public class NotificationTarget {
    private String id;
    private String name;
    private String url;
    /**
     * If username and password are set, Basic Authentication is used.
     */
    private String username;
    private String password;
    private boolean enabled;
    /**
     * not really a domain member, but handy as a shortcut for the moment.
     */
    private boolean preExisting;

    @Override
    public String toString() {
        return "[" + this.id + ", " + this.name + ", " + this.url + ", " + this.enabled + ", " + this.preExisting + "]";
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        NotificationTarget that = (NotificationTarget) o;

        if (id != null ? !id.equals(that.id) : that.id != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return id != null ? id.hashCode() : 0;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public boolean isPreExisting() {
        return preExisting;
    }

    public void setPreExisting(boolean preExisting) {
        this.preExisting = preExisting;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }
}
